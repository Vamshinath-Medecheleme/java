public class Switch10 {
    public static void main(String[] args) {
        
        int num=3;
        String day;

        switch(num){

            case 1:day="sunday";
            break;
            case 2:day="monday";
            break;
            case 3:day="tuesday";
            break;
            default:day="sunnaday";
        
        }
        System.out.println(day);
    }
    
}
