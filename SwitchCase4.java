public class SwitchCase4 {
    public static void main(String[] args) {

        int num=2;
        String day;

        switch (num) {
            case 1:
            day="sunday";
                break;

                case 2:
                day="monday";
                break;

                case 3:
                day="tuesday";
        
            default:
            day="raviteja";
                break;
        }
        System.out.println(day);
    }
    
}
